export async function scoreInput(
  prompt: string,
  input: string,
  criteria: string[]
): Promise<ScoreResult> {
  // Placeholder: In production, send to LLM for multi-criteria scoring
  const breakdown: Record<string, number> = {};
  let total = 0;
  for (const criterion of criteria) {
    const score = 70 + Math.floor(criterion.length % 30); // deterministic mock
    breakdown[criterion] = score;
    total += score;
  }

  return {
    score: criteria.length > 0 ? Math.round(total / criteria.length) : 75,
    breakdown,
  };
}

export async function draftContent(
  prompt: string,
  context: Record<string, unknown>
): Promise<DraftResult> {
  // Placeholder: In production, send prompt + context to LLM for content generation
  const contextKeys = Object.keys(context).join(', ');

  return {
    content: `[AI Draft] Based on prompt "${prompt}" with context keys: ${contextKeys}. This is a placeholder draft that would be generated by an LLM in production.`,
    confidence: 0.75,
  };
}

export async function summarizeInput(
  prompt: string,
  input: string,
  maxLength?: number
): Promise<SummaryResult> {
  // Placeholder: In production, send to LLM for summarization
  const truncated = input.substring(0, maxLength ?? 200);

  return {
    summary: `[AI Summary] ${prompt}: ${truncated}${input.length > (maxLength ?? 200) ? '...' : ''}`,
  };
}
