import { generateJSON } from '@/lib/ai';
import type { ImpersonationSafeguard } from './types';

export interface ImpersonationAnalysis {
  isLikelyImpersonation: boolean;
  confidence: number;
  reasoning: string;
  styleDeviations: string[];
}

// In-memory consent store (placeholder for database-backed storage)
const consentStore = new Map<string, { voiceCloneId: string; consentTimestamp: Date }>();
const watermarkStore = new Set<string>();

export async function verifyVoiceCloneConsent(
  userId: string,
  voiceCloneId: string
): Promise<ImpersonationSafeguard> {
  const key = `${userId}:${voiceCloneId}`;
  const consent = consentStore.get(key);

  return {
    consentVerified: !!consent,
    watermarkApplied: watermarkStore.has(voiceCloneId),
    disclosureIncluded: false,
    voiceCloneId,
    consentTimestamp: consent?.consentTimestamp,
  };
}

export async function applyWatermark(audioContentId: string): Promise<ImpersonationSafeguard> {
  watermarkStore.add(audioContentId);

  return {
    consentVerified: true,
    watermarkApplied: true,
    disclosureIncluded: true,
    voiceCloneId: audioContentId,
  };
}

export function generateDisclosure(context: string): string {
  const disclosures: Record<string, string> = {
    voice_call: 'This call is being handled by an AI assistant on behalf of the user. The voice you hear is AI-generated.',
    email: 'This message was drafted by an AI assistant and sent on behalf of the user.',
    chat: 'You are communicating with an AI assistant acting on behalf of the user.',
    voicemail: 'This voicemail was generated by an AI assistant on behalf of the user.',
  };

  return disclosures[context] ??
    `This communication was generated by an AI assistant. Content may be AI-generated and should be verified.`;
}

export async function detectImpersonation(
  baselineMessages: string[],
  suspectMessage: string
): Promise<ImpersonationAnalysis> {
  try {
    return await generateJSON<ImpersonationAnalysis>(
      `Compare the communication style of a baseline user profile against a suspect message to assess impersonation likelihood.

Baseline messages (known user style):
${baselineMessages.slice(0, 5).map((m, i) => `${i + 1}. "${m}"`).join('\n')}

Suspect message:
"${suspectMessage}"

Analyze vocabulary, sentence structure, tone, and writing patterns. Return a JSON object with:
- isLikelyImpersonation: boolean
- confidence: number 0-1
- reasoning: explanation of assessment
- styleDeviations: array of specific style differences found`,
      { temperature: 0.2 }
    );
  } catch {
    return {
      isLikelyImpersonation: false,
      confidence: 0,
      reasoning: 'AI analysis unavailable, falling back to default safe assessment.',
      styleDeviations: [],
    };
  }
}

// Helper to record consent (used by API routes)
export function recordConsent(userId: string, voiceCloneId: string): void {
  const key = `${userId}:${voiceCloneId}`;
  consentStore.set(key, { voiceCloneId, consentTimestamp: new Date() });
}
